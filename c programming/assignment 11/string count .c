//WAP to Count the Number of Vowels in a String
#include<stdio.h>
#include<string.h>
void main()
{
	char str[30];
	int count;
	printf("Enter a string");
	scanf("%s",str);
	printf("Enter count number of vowels");
	scanf("%d",&count);
	{
		for(int i=0;i<count;i++)
		{
			if(str[0]==count)
		{
		break;
		}
		printf("Count the Number of Vowels in a String");
		printf("%d",count);
	}
}
}